package vo;

public class TheaterVo {
	
	int t_idx;
	String t_name;
	String t_seat;
	int c_idx;
	
	
	public int getT_idx() {
		return t_idx;
	}
	public void setT_idx(int t_idx) {
		this.t_idx = t_idx;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String t_name) {
		this.t_name = t_name;
	}
	public String getT_seat() {
		return t_seat;
	}
	public void setT_seat(String t_seat) {
		this.t_seat = t_seat;
	}
	public int getC_idx() {
		return c_idx;
	}
	public void setC_idx(int c_idx) {
		this.c_idx = c_idx;
	}
	
	

}
