package vo;

public class MovieVo {
	int m_idx;
	String m_name;
	String m_rate;
	String m_release;
	String m_r_time;
	String m_age;
	int c_idx;
	int t_idx;
	
	
	public int getM_idx() {
		return m_idx;
	}
	public void setM_idx(int m_idx) {
		this.m_idx = m_idx;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
	public String getM_rate() {
		return m_rate;
	}
	public void setM_rate(String m_rate) {
		this.m_rate = m_rate;
	}
	public String getM_release() {
		return m_release;
	}
	public void setM_release(String m_release) {
		this.m_release = m_release;
	}
	public String getM_r_time() {
		return m_r_time;
	}
	public void setM_r_time(String m_r_time) {
		this.m_r_time = m_r_time;
	}
	public String getM_age() {
		return m_age;
	}
	public void setM_age(String m_age) {
		this.m_age = m_age;
	}
	public int getC_idx() {
		return c_idx;
	}
	public void setC_idx(int c_idx) {
		this.c_idx = c_idx;
	}
	public int getT_idx() {
		return t_idx;
	}
	public void setT_idx(int t_idx) {
		this.t_idx = t_idx;
	}
	
}
